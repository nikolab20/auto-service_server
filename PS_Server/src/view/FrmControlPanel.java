    public void preparePanel() {
        resourceBundle = ResourceBundle.getBundle("props/LanguageBundle", Controller.getInstance().getLocale());

        btnStart.setText(resourceBundle.getString("server_btn_start"));
        btnKick.setText(resourceBundle.getString("server_btn_kick"));
        btnStop.setText(resourceBundle.getString("server_btn_stop"));

        URL imageUrl = ClassLoader.getSystemResource("img/transportation.png");
        ImageIcon icon = new ImageIcon(imageUrl);
        setIconImage(icon.getImage());

        setLocationRelativeTo(null);
        serverThread.addListener(this);
        tms = new TableModelSocket(serverThread.getClients());
        tableConnections.setModel(tms);
        checkNewClientsThread = new CheckNewClientsThread(serverThread, tms);
        checkNewClientsThread.start();
    }

    public void clearPanel() {
        tms.updateTable(new ArrayList<>());
    }

    @Override
    public void onClientConnected() {
        tms.updateTable(serverThread.getClients());
    }

