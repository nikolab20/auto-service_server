    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        if (!serverThread.isAlive()) {
            try {
                serverThread = new ServerThread();
                serverThread.start();
                checkNewClientsThread = new CheckNewClientsThread(serverThread, tms);
                checkNewClientsThread.start();
                btnStart.setEnabled(false);
                btnStop.setEnabled(true);
                btnKick.setEnabled(true);
            } catch (IOException ex) {
                MessageDialog.showErrorMessage(null, resourceBundle.getString("server_connected_error"),
                        resourceBundle.getString("error_title"));
            }
        }
    }//GEN-LAST:event_btnStartActionPerformed

    private void btnStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopActionPerformed
        if (serverThread.isAlive()) {
            try {
                serverThread.stopServerThread();
                checkNewClientsThread.interrupt();
                btnStart.setEnabled(true);
                btnStop.setEnabled(false);
                btnKick.setEnabled(false);
            } catch (IOException ex) {
                MessageDialog.showErrorMessage(null, resourceBundle.getString("server_stop_problem"),
                        resourceBundle.getString("error_title"));
            }
        }
    }//GEN-LAST:event_btnStopActionPerformed

    private void btnKickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKickActionPerformed
        int selectedRow = tableConnections.getSelectedRow();

        if (selectedRow != -1) {
            try {
                ClientHandlerThread client = serverThread.getClients().get(selectedRow);
                client.getSocket().close();
            } catch (IOException ex) {
                MessageDialog.showErrorMessage(null, resourceBundle.getString("server_kick_problem"),
                        resourceBundle.getString("error_title"));
            }
        }
    }//GEN-LAST:event_btnKickActionPerformed

